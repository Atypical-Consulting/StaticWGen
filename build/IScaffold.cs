using System;
using System.Globalization;
using System.IO;
using System.Linq;
using System.Text;
using System.Text.RegularExpressions;
using Nuke.Common;
using Nuke.Common.IO;
using static Serilog.Log;

public interface IScaffold : IHasWebsitePaths
{
    [Parameter("Title for the new content")]
    string Title => TryGetValue(() => Title) ?? "";

    [Parameter("Tags for the new post (comma-separated)")]
    string Tags => TryGetValue(() => Tags) ?? "";

    [Parameter("Author name")]
    string Author => TryGetValue(() => Author) ?? "";

    Target NewPost => _ => _
        .Executes(() =>
        {
            Assert.True(!string.IsNullOrEmpty(Title), "Title is required. Use --title \"My Post Title\"");

            var slug = GenerateSlug(Title);
            var date = DateTime.Today.ToString("yyyy-MM-dd");
            var fileName = $"{date}-{slug}.md";
            var filePath = InputDirectory / fileName;

            Assert.True(!filePath.FileExists(), $"File already exists: {fileName}");

            var content = $"""
                ---
                title: "{Title}"
                description: ""
                author: "{Author}"
                date: "{date}"
                keywords: "{Tags}"
                image: ""
                draft: true
                ---
                # {Title}

                Write your content here.
                """;

            filePath.WriteAllText(content);
            Information("Created new post: {FilePath}", filePath);
            Information("  Title: {Title}", Title);
            Information("  Date:  {Date}", date);
            Information("  Slug:  {Slug}", slug);
            Information("  Draft: true (publish by setting draft: false)");
        });

    Target NewPage => _ => _
        .Executes(() =>
        {
            Assert.True(!string.IsNullOrEmpty(Title), "Title is required. Use --title \"About Us\"");

            var slug = GenerateSlug(Title);
            var fileName = $"{slug}.md";
            var filePath = InputDirectory / fileName;

            Assert.True(!filePath.FileExists(), $"File already exists: {fileName}");

            var content = $"""
                ---
                title: "{Title}"
                description: ""
                author: "{Author}"
                keywords: "{Tags}"
                image: ""
                ---
                # {Title}

                Write your content here.
                """;

            filePath.WriteAllText(content);
            Information("Created new page: {FilePath}", filePath);
            Information("  Title: {Title}", Title);
            Information("  Slug:  {Slug}", slug);
        });

    Target Init => _ => _
        .Executes(() =>
        {
            var siteTitle = !string.IsNullOrEmpty(Title) ? Title : "My Static Website";

            // Create input directory if it doesn't exist
            InputDirectory.CreateDirectory();

            // Create index.md
            var indexPath = InputDirectory / "index.md";
            if (!indexPath.FileExists())
            {
                indexPath.WriteAllText($"""
                    ---
                    title: "{siteTitle}"
                    description: "Welcome to {siteTitle}"
                    author: "{Author}"
                    date: "{DateTime.Today:yyyy-MM-dd}"
                    keywords: ""
                    image: ""
                    ---
                    # Welcome to {siteTitle}

                    This is a static website generated by [StaticWGen](https://github.com/phmatray/StaticWGen).

                    ## Getting Started

                    Edit this file at `input/index.md` to customize your homepage.
                    """);
                Information("Created: input/index.md");
            }

            // Create about.md
            var aboutPath = InputDirectory / "about.md";
            if (!aboutPath.FileExists())
            {
                aboutPath.WriteAllText($"""
                    ---
                    title: "About"
                    description: "About {siteTitle}"
                    author: "{Author}"
                    keywords: "about"
                    ---
                    # About

                    Tell visitors about your site.
                    """);
                Information("Created: input/about.md");
            }

            // Create assets directory
            var assetsDir = InputDirectory / "assets";
            assetsDir.CreateDirectory();
            Information("Created: input/assets/");

            Information("");
            Information("Project initialized! Next steps:");
            Information("  1. Edit input/index.md and input/about.md");
            Information("  2. Run: ./build.sh BuildWebsite --site-base-url \"http://localhost:8080\" --site-title \"{siteTitle}\"");
            Information("  3. Open output/index.html in your browser");
        });

    static string GenerateSlug(string title)
    {
        // Normalize unicode characters
        var normalized = title.Normalize(NormalizationForm.FormD);
        var sb = new StringBuilder();
        foreach (var c in normalized)
        {
            var category = char.GetUnicodeCategory(c);
            if (category != UnicodeCategory.NonSpacingMark)
                sb.Append(c);
        }

        var slug = sb.ToString().Normalize(NormalizationForm.FormC).ToLowerInvariant();

        // Replace common programming terms
        slug = slug.Replace("c#", "csharp").Replace("f#", "fsharp").Replace(".net", "dotnet");

        // Replace non-alphanumeric with hyphens
        slug = Regex.Replace(slug, @"[^a-z0-9]+", "-");

        // Trim hyphens
        slug = slug.Trim('-');

        return slug;
    }
}
